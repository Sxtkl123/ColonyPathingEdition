package com.arxyt.colonypathingedition.core.config.enums;

public enum BuilderModeEnum {

    /**
     * 常规：与现有殖民地没啥区别。
     */
    NORMAL,

    /**
     * 形式主义者：建筑工会颇具表演天赋，在工地假装上蹿下跳，其实在出发的一瞬间就已经开始放置方块了。
     */
    FORMALIST,

    /**
     * 哨兵：建筑工会像个哨兵一样，在工地站着不动进行建筑，又或者走到自己的小屋前等待补货，从效率来看会比形式主义者慢。
     */
    SENTRY,

    /**
     * 神：建筑工可以在自己的小屋手眼通天，直接建造工地的东西。
     */
    GOD,

    /**
     * 长臂猿：建筑工会像猿猴一样在工地上蹿下跳，但是手又很长。
     */
    GIBBON,

}
